/**
 * Populates the exercises table with the 214-entry library on first install.
 * Idempotent: returns early if seedStarted flag set or table already populated.
 * 2-pass: parents inserted first (parentExerciseId: null), then variants resolve
 * parentExerciseId via name lookup against the just-inserted parents.
 * Throws if a variant references an unknown parent name.
 */
export async function seedExercises(): Promise<void> {
  if (seedStarted) return;
  seedStarted = true;
  const count = await db.exercises.count();
  if (count > 0) return;

  const parents = SEED_ENTRIES.filter((entry) => entry.parentName === null);
  const variants = SEED_ENTRIES.filter((entry) => entry.parentName !== null);

  const parentRecords: Omit<Exercise, 'id'>[] = parents.map(({ parentName: _p, ...rest }) => ({
    ...rest,
    parentExerciseId: null,
  }));
  await db.exercises.bulkAdd(parentRecords);

  const inserted = await db.exercises.toArray();
  const nameToId = new Map<string, number>();
  for (const ex of inserted) {
    if (ex.id !== undefined) nameToId.set(ex.name, ex.id);
  }

  const variantRecords: Omit<Exercise, 'id'>[] = variants.map(({ parentName, ...rest }) => {
    const parentId = nameToId.get(parentName!);
    if (parentId === undefined) {
      throw new Error(`Variant "${rest.name}" references unknown parent "${parentName}"`);
    }
    return { ...rest, parentExerciseId: parentId };
  });
  await db.exercises.bulkAdd(variantRecords);
}

/** Test-only: resets the module-level guard so tests can re-run seedExercises after a db.delete(). */
export function _resetSeedFlagForTest(): void {
  seedStarted = false;
}
