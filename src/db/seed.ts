// ── Helpers ────────────────────────────────────────────────────────────────

const s = (m: Muscle): SecondaryMuscle => ({ muscle: m, role: 'synergist' });
const b = (m: Muscle): SecondaryMuscle => ({ muscle: m, role: 'stabilizer' });

type SeedEntry = Omit<Exercise, 'id' | 'parentExerciseId'> & {
  parentName: string | null;
};

type Tier3 = Partial<Pick<Exercise, 'gripWidth' | 'gripOrientation' | 'stanceWidth' | 'bias'>>;

function e(
  name: string,
  parentName: string | null,
  primary: Muscle[],
  secondary: SecondaryMuscle[],
  equipment: string | null,
  tier3: Tier3 = {},
): SeedEntry {
  return {
    name,
    isCustom: false,
    parentName,
    primaryMuscles: primary,
    secondaryMuscles: secondary,
    equipment,
    gripWidth: tier3.gripWidth ?? null,
    gripOrientation: tier3.gripOrientation ?? null,
    stanceWidth: tier3.stanceWidth ?? null,
    bias: tier3.bias ?? null,
    jointLoad: [],
  };
}

